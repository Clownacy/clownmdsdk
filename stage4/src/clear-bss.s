	| Clear BSS section.
	lea	(_BSS_START_).l,%a1
	move.w	#_BSS_LOOP_COUNT_,%d0
	moveq	#_BSS_LOOP_OFFSET_,%d1
	moveq	#0,%d2
	jmp	2f(%pc,%d1.w)
1:
	move.l	%d2,(%a1)+
	move.l	%d2,(%a1)+
	move.l	%d2,(%a1)+
	move.l	%d2,(%a1)+
2:
	dbf	%d0,1b
